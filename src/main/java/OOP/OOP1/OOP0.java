// Object-Orientated Programming (Lập trình Hướng đối tượng)
// Là phương pháp lập trình đưa các đối tượng có thực vào trong lập trình
// Lập trình hướng đối tượng là 1 phương pháp tạo ra các class, object, ...
// - Class: Lớp: có thể hiểu class là 1 kiểu dữ liệu do người dùng định nghĩa
// - Class chứa: thông tin và hành động của một đối tượng (thông tin = thuộc tính, hành động = phương thức)
// - Object: Đối tượng
// Mối quan hệ giữa Class và Object:
// - Class thể hiện một tập hợp các Object có những thuộc tính, phương thức giống nhau
// Cách tạo Class:
//    <Access Modifier (thường là public)> class <tên Class> {
//    thuộc tính;
//    phương thức;
//        }
// Cách tạo Object:
//  <tên Class> <tên Object> = new <tên Class>();
// Tạo 1 Class: Con người gồm các thuộc tính: tên, tuổi, địa chỉ, SĐT và các phương thức: ăn, ngủ, chơi
//  <Access Modifier> <kiểu dữ liệu> <tên thuộc tính>
//      public              int             a
// Constructor: phương thức đặc biệt dùng để khởi tạo Object, luôn luôn tồn tại ít nhất 1 Constructor trong Class
